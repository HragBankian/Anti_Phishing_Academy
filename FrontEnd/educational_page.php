        <h1>What Is Phishing?</h1>
        <p style="color:black; text-align:center">Phishing is a type of cyber attack where a perpetrator, typically disguised as a trustworthy entity, attempts to trick individuals into providing sensitive information such as passwords, credit card numbers, or other personal details. This is often done through deceptive emails, messages, or websites that appear to be from legitimate sources.</p>
        <p style="color:black; text-align:center">The goal of phishing is to gain access to sensitive information that can be used for fraudulent purposes, such as identity theft, financial fraud, or unauthorized access to accounts. Phishing attacks often rely on social engineering tactics to exploit human psychology, such as creating a sense of urgency or using official-looking logos and language to deceive the victim.</p>
    </div>
    <div class="doup">
        <h2>Features to Look Out For In Emails:</h2>
        <ul>
            <li><a style="font-weight:bold;">Unexpected Requests:</a> Be wary of unexpected emails requesting personal information or urgent action.</li>
            <li><a style="font-weight:bold;">Misspelled URLs:</a> Check the URL of links in emails for misspellings or unusual characters.</li>
            <li><a style="font-weight:bold;">Generic Greetings:</a> Phishing emails often use generic greetings like "Dear Customer" instead of your name.</li>
            <li><a style="font-weight:bold;">Threats or Urgency:</a> Watch out for emails that create a sense of urgency or threaten negative consequences if you don't act immediately.</li>
            <li><a style="font-weight:bold;">Attachments or Downloads:</a> Avoid opening attachments or downloading files from unfamiliar or suspicious emails.</li>
            <li><a style="font-weight:bold;">Poor Grammar or Spelling Errors:</a> Many phishing emails originate from non-native English speakers or automated systems, resulting in grammatical errors, awkward phrasing, or spelling mistakes. Legitimate communications from reputable organizations typically undergo professional editing and proofreading.</li>
